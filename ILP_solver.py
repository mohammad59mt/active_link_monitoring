def log_everything(lp_problem, route_entries=None):
    # --------------------------------------------------------------------------------------------
    # logging route entries
    logger.setLevel(logging.INFO)
    if route_entries is not None: logger.info('Rules: {}'.format(str(route_entries)))
    # logging BLP results
    logger.info('Problem Solving Status: {}'.format(pulp.LpStatus[lp_problem.status]))
    logger.info("Objective function value: {}".format(pulp.value(lp_problem.objective)))
    logger.info('Variables: ')
    for variable in lp_problem.variables():
        if int(variable.varValue) is not 0:
            logger.info("   {} = {}".format(variable.name, variable.varValue))
    # --------------------------------------------------------------------------------------------
    # logging problem
    logger.setLevel(logging.DEBUG)
    logger.debug(lp_problem)



